export function slugifyStem(name) {
  return name
    .toLowerCase()
    .normalize('NFD')
    .replace(/\p{M}/gu, '')
    .replace(/_/g, '-')
    .replace(/[^a-z0-9-]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

export function lookupMyrionManifest(stem) {
  const slug = slugifyStem(stem)
  if (MYRION_MANIFEST_BY_STEM[slug]) return MYRION_MANIFEST_BY_STEM[slug]
  // cœur → c-ur après slugify
  const alt = slug.replace(/-ur-de-/, '-oeur-de-').replace(/-c-ur-/, '-coeur-')
  return MYRION_MANIFEST_BY_STEM[alt] ?? null
}
